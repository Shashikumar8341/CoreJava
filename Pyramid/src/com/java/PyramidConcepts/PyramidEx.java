package com.java.PyramidConcepts;

public class PyramidEx {
	public static void main(String[] args) {
		for (int i = 0; i <= 5; i++) {
			for (int j = 1; j <= 5; j++) {
				System.out.print("*");
			}
			System.out.print("\n");
		}
		System.out.println("    ");

		for (int l = 0; l <= 3; l++) {
			for (int j = 1; j <= 3; j++) {
				System.out.print(" * ");
			}
			System.out.print("\n");
		}
	}
}
