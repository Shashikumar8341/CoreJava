package com.java.CastingConcepts;

public class CastingDemo {
	public static void main(String[] args) {
//type casting
		long d=8341866042L;
		double f=2564.00D;
		int h=1;
		short k=26486;
		float number=586.0f;

		System.out.println((int)f);  
		System.out.println((double)d);
		System.out.println((short)number);
		System.out.println((float)k);
		System.out.println((long)k);
		System.out.println((double)h);
	}
}
