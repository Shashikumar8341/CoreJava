package com.java.Arrays;

public class ForLoopArray {
public static void main(String[] args) {
	String name[][]=new String[2][5] ;
	 name[0][0] = "name";
	 name[1][1] = "name";
	 name[1][2] = "name";
	 
     System.out.println("arr[0][0] = " + name[0][0]);
     System.out.println("arr[0][0] = " + name[1][1]);
     System.out.println("arr[0][0] = " + name[1][2]);
	}
		
	
}

