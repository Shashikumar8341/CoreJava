package com.java.operators;

public class RightLiftShiftoperator {
public static void main(String[] args) {
int  i=10;
int j=2;
	System.out.println(i<<j);//lift shift
	System.out.println(i>>j>>j);//right shift
	System.out.println(+i<<2);//lift shift
	System.out.println(i>>>j);//right shift

}
}
