package com.java.LoopConcepts;

public class ForloopOddNumber {
	public static void main(String[] args) {
		
	for (int j = 2; j < 50; j=j+2) {
		System.out.print(j+" ");
		
	}		
		System.out.println();
// odd numbers in For loop

		for (int i = 1; i < 50; i = i + 2) {
			System.out.print(i+" ");
		}
	}}
